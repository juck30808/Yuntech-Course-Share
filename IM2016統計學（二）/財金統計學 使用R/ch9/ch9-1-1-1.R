# Using fGarch Package
# t distribution
library(fGarch)
set.seed(123)
x  = rnorm(1000)
x1 = rstd(1000,nu=3)
x2 = rstd(1000,nu=4)
x3 = rstd(1000,nu=5)
x4 = rstd(1000,nu=10)
x5 = rstd(1000,nu=20)
windows()
par(mfrow=c(2,1))
plot(density(x),type="l",xlab="",ylim=c(0,0.7),main="舡pぇ夹非`AだtPぃP邾勋驻U杭蟹tだt",lwd=2)
lines(density(x1),lty=2,col="2",lwd=2)
lines(density(x2),lty=3,col="3",lwd=2)
lines(density(x3),lty=4,col="4",lwd=2)
lines(density(x4),lty=5,col="5",lwd=2)
lines(density(x5),lty=6,col="6",lwd=2)
legend("topright",c("夹非`A","v = 3, t","v = 4","v = 5","v = 10","v = 20"), lty=1:6, lwd=2,
       col=1:6, bty="n")

y = seq(-4,4,length=1000)
xy  = dnorm(y)
y1 = dstd(y,nu=3)
y2 = dstd(y,nu=4)
y3 = dstd(y,nu=5)
y4 = dstd(y,nu=10)
y5 = dstd(y,nu=20)
plot(y,xy,type="l",xlab="",ylim=c(0,0.015),xlim=c(3.5,4), lwd=2,
     main="夹非`AだtPぃP邾勋驻U杭蟹tだt (kЮ)")
lines(y,y1,lty=2,col="2",lwd=2)
lines(y,y2,lty=3,col="3",lwd=2)
lines(y,y3,lty=4,col="4",lwd=2)
lines(y,y4,lty=5,col="5",lwd=2)
lines(y,y5,lty=6,col="6",lwd=2)
legend("topright",c("夹非`A","v = 3, t","v = 4","v = 5","v = 10","v = 20"), lty=1:6, lwd=2,
       col=1:6, bty="n")
